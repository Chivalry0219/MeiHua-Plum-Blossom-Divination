export function getEarthlyBranch(hour: number) {
  if (hour >= 23 || hour < 1) return EARTHLY_BRANCHES[0];
  const index = Math.floor((hour + 1) / 2);
  return EARTHLY_BRANCHES[index];
}

export function getTrigramFromLines(lines: number[]) {
  return TRIGRAMS.find(t => t.lines.every((l, i) => l === lines[i]))?.id || 1;
}

export function calculateMutual(upperId: number, lowerId: number) {
  const upperLines = TRIGRAMS.find(t => t.id === upperId)!.lines;
  const lowerLines = TRIGRAMS.find(t => t.id === lowerId)!.lines;
  const allLines = [...lowerLines, ...upperLines];
  const mutualLowerLines = [allLines[1], allLines[2], allLines[3]];
  const mutualUpperLines = [allLines[2], allLines[3], allLines[4]];
  return {
    upperId: getTrigramFromLines(mutualUpperLines),
    lowerId: getTrigramFromLines(mutualLowerLines)
  };
}

export function calculateTransformed(upperId: number, lowerId: number, movingLine: number) {
  const upperLines = [...TRIGRAMS.find(t => t.id === upperId)!.lines];
  const lowerLines = [...TRIGRAMS.find(t => t.id === lowerId)!.lines];
  const allLines = [...lowerLines, ...upperLines];
  const idx = movingLine - 1;
  allLines[idx] = allLines[idx] === 1 ? 0 : 1;
  return {
    lowerId: getTrigramFromLines(allLines.slice(0, 3)),
    upperId: getTrigramFromLines(allLines.slice(3, 6))
  };
}

export interface DivinationResult {
  strokes: { char: string; count: number }[];
  totalStrokes: number;
  upperTrigramId: number;
  lowerTrigramId: number;
  movingLine: number;
  analysis: string;
}

export function localDivination(text: string, hour: number): DivinationResult {
  const chars = text.split('');
  const strokes = chars.map(char => {
    const count = cnchar.stroke(char) as number;
    return { char, count: typeof count === 'number' ? count : 0 };
  });

  const totalStrokes = strokes.reduce((acc, s) => acc + s.count, 0);
  const branch = getEarthlyBranch(hour);
  const timeValue = branch.value;

  // Plum Blossom Logic
  let upperId = totalStrokes % 8;
  if (upperId === 0) upperId = 8;

  let lowerId = (totalStrokes + timeValue) % 8;
  if (lowerId === 0) lowerId = 8;

  let movingLine = (totalStrokes + timeValue) % 6;
  if (movingLine === 0) movingLine = 6;

  const originalKey = `${upperId}${lowerId}`;
  const originalHex = HEXAGRAMS[originalKey];
  const originalName = originalHex?.name || "未知卦";
  const originalJudgment = originalHex?.judgment || "暂无原文。";
  const originalMeaning = originalHex?.meaning || "暂无详细卦意解读。";
  
  const mutual = calculateMutual(upperId, lowerId);
  const mutualKey = `${mutual.upperId}${mutual.lowerId}`;
  const mutualHex = HEXAGRAMS[mutualKey];
  const mutualName = mutualHex?.name || "未知卦";
  const mutualJudgment = mutualHex?.judgment || "暂无原文。";
  const mutualMeaning = mutualHex?.meaning || "暂无详细卦意解读。";
  
  const transformed = calculateTransformed(upperId, lowerId, movingLine);
  const transformedKey = `${transformed.upperId}${transformed.lowerId}`;
  const transformedHex = HEXAGRAMS[transformedKey];
  const transformedName = transformedHex?.name || "未知卦";
  const transformedJudgment = transformedHex?.judgment || "暂无原文。";
  const transformedMeaning = transformedHex?.meaning || "暂无详细卦意解读。";

  const yaoStatement = originalHex?.yaoStatements[movingLine] || "（爻辞推演中：请参考经典易经原文）";
  const yaoMeaning = originalHex?.yaoMeanings[movingLine] || "此爻发动，预示着当前局势的关键转折点，需结合卦象整体推敲。";

  // 体用生克逻辑
  const upperTrigram = TRIGRAMS.find(t => t.id === upperId)!;
  const lowerTrigram = TRIGRAMS.find(t => t.id === lowerId)!;
  
  // 动爻在1-3爻，下卦为用，上卦为体；动爻在4-6爻，上卦为用，下卦为体
  const isMovingInUpper = movingLine >= 4;
  const bodyTrigram = isMovingInUpper ? lowerTrigram : upperTrigram;
  const useTrigram = isMovingInUpper ? upperTrigram : lowerTrigram;

  const getInteraction = (body: string, use: string) => {
    const relations: Record<string, string[]> = {
      "木": ["火", "土"], // 生火, 克土
      "火": ["土", "金"], // 生土, 克金
      "土": ["金", "水"], // 生金, 克水
      "金": ["水", "木"], // 生水, 克木
      "水": ["木", "火"], // 生木, 克火
    };

    if (body === use) return { type: "比和", desc: "体用比和，百事顺遂，大吉之象。" };
    if (relations[use][0] === body) return { type: "用生体", desc: "用生体，为进益之兆，多得助力，所求易成。" };
    if (relations[body][0] === use) return { type: "体生用", desc: "体生用，为泄气之兆，劳心劳力，财物易散。" };
    if (relations[body][1] === use) return { type: "体克用", desc: "体克用，为胜任之兆，虽有波折，终能克服，所求可成。" };
    if (relations[use][1] === body) return { type: "用克体", desc: "用克体，为受制之兆，多有阻碍，诸事不利，需防灾厄。" };
    return { type: "未知", desc: "五行生克推演中..." };
  };

  const interaction = getInteraction(bodyTrigram.element!, useTrigram.element!);

  const analysis = `
【本卦：${originalName}】
易经原文：${originalJudgment}
现代解读：${originalMeaning}
（本卦代表事物的现状与起势面貌）

【体用生克】
体卦：${bodyTrigram.name}（${bodyTrigram.element}）
用卦：${useTrigram.name}（${useTrigram.element}）
关系：${interaction.type}
解读：${interaction.desc}

【互卦：${mutualName}】
易经原文：${mutualJudgment}
现代解读：${mutualMeaning}
（互卦代表事物发展的中间过程与内在交互及事情本质）

【变卦：${transformedName}】
易经原文：${transformedJudgment}
现代解读：${transformedMeaning}
（变卦代表事物的最终趋向与结果）

【动爻：第${movingLine}爻】
爻辞原文：${yaoStatement}
现代解读：${yaoMeaning}

梅花推演：
上卦为${upperTrigram.name}（${upperTrigram.nature}），下卦为${lowerTrigram.name}（${lowerTrigram.nature}）。
起卦总笔画为${totalStrokes}，时辰为${branch.name}时（序数${timeValue}）。
  `.trim();

  return {
    strokes,
    totalStrokes,
    upperTrigramId: upperId,
    lowerTrigramId: lowerId,
    movingLine,
    analysis
  };
}
